    CREATE TABLE
    UNIT
    (
        UNIT_ID VARCHAR2(7 CHAR) NOT NULL,
        NAME VARCHAR2(20 CHAR) NOT NULL,
        PRIMARY KEY (UNIT_ID)
    );
    
    CREATE TABLE
    EMPLOYEE
    (
        EMP_ID VARCHAR2(7 CHAR) NOT NULL,
        NAME VARCHAR2(20 CHAR) NOT NULL,
        UNIT_ID VARCHAR2(7 CHAR),
        PRIMARY KEY (EMP_ID),
        CONSTRAINT FKTIQ7IGB9XWOFHJ3HOP5L9BU87 FOREIGN KEY (UNIT_ID) REFERENCES UNIT (UNIT_ID)
    );
    
    
    CREATE TABLE
    CASE_REQ
    (
        CASE_NO VARCHAR2(11 CHAR) NOT NULL,
        END_DATE TIMESTAMP(6) NOT NULL,
        MONITOR_LEVEL NUMBER(10) NOT NULL,
        START_DATE TIMESTAMP(6) NOT NULL,
        TYPE NUMBER(10) NOT NULL,
        WORK_ITEM_DESC VARCHAR2(200 CHAR) NOT NULL,
        CONTACT_EMP_ID VARCHAR2(7 CHAR),
        MANAGER_EMP_ID VARCHAR2(7 CHAR),
        PRIMARY KEY (CASE_NO),
        CONSTRAINT FK8QPYF5EQBACFSQUPIUYCKPT99 FOREIGN KEY (CONTACT_EMP_ID) REFERENCES EMPLOYEE
        (EMP_ID),
        CONSTRAINT FKK5NY4NUFO2AOB3D4MXQYL0U5W FOREIGN KEY (MANAGER_EMP_ID) REFERENCES EMPLOYEE
        (EMP_ID)
    );




